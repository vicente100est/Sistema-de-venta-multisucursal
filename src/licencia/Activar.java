    private void btnLicenciaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLicenciaActionPerformed
        if (txtLicencia.getText().isEmpty()) {
            lblError.setIcon( new ImageIcon(getClass().getResource("/img/licencias/label-expirado.png")));
            txtLicencia.requestFocus();
            lblError.setText("Ingrese una Licencia");
        } else {
            String lic = licencia.MD5.getEncriptar(txtLicencia.getText());

            new CargandoLicencia(new JFrame(), true).setVisible(true);

            if (licencia.Verificar.getLicencia().equals(lic)) {
                if (licencia.Verificar.generaLicencia()) {
                    principal.Principal.verificaLicencia();
                    this.dispose();
                }
            } else {
                lblError.setIcon( new ImageIcon(getClass().getResource("/img/licencias/label-expirado.png")));
                txtLicencia.setText("");
                txtLicencia.requestFocus();
                lblError.setText("Licencia no valida");
            }
        }
    }//GEN-LAST:event_btnLicenciaActionPerformed

