    private boolean isCambiado() {
        Sentencias s = new Sentencias();

        s.setUsuario(this.txtUsuario.getText());
        s.setId(Integer.parseInt(principal.Principal.lblID.getText()));

        if (Operaciones.isUsuarioCambiado(s)) {
            return true;
        } else {
            return false;
        }
    }

