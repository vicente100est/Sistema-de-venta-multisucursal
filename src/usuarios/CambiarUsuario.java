    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if (isLlenado()) {
            if (Operaciones.isExiste(this.txtUsuario.getText())) {
                this.txtUsuario.requestFocus();
                lblError.setIcon(new ImageIcon(getClass().getResource("/img/login/error.png")));
                lblError.setBackground(new Color(255, 76, 76));
                lblError.setForeground(new Color(255, 255, 255));
                this.lblError.setText("¡El nombre de usuario elegido ya éxiste!");
            } else {
                if (Operaciones.verificaPassword(principal.Principal.lblID.getText()).equals(this.txtPass.getText())) {
                    if (isCambiado()) {
                        this.dispose();
                        s.msj1.setText("Nombre de usuario cambiado");
                        s.msj2.setText("con éxito.");
                        s.msj3.setText("");
                        s.setVisible(true);
                    } else {
                        this.dispose();
                        e.msj1.setText("Algo salio mal. No fue posible");
                        e.msj2.setText("guardar los cambios.");
                        e.msj3.setText("");
                        e.setVisible(true);
                    }
                } else {
                    this.txtPass.requestFocus();
                    lblError.setIcon(new ImageIcon(getClass().getResource("/img/login/error.png")));
                    lblError.setBackground(new Color(255, 76, 76));
                    lblError.setForeground(new Color(255, 255, 255));
                    this.lblError.setText("¡La contraseña ingresada no es correcta!");
                }
            }
        } else {
            this.txtUsuario.requestFocus();
            lblError.setIcon(new ImageIcon(getClass().getResource("/img/login/error.png")));
            lblError.setBackground(new Color(255, 76, 76));
            lblError.setForeground(new Color(255, 255, 255));
            this.lblError.setText("¡Debes llenar ambos campos!");
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

