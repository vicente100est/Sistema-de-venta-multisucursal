    private void addEventKey() {
        //--------------------------------------------------------------------------------
        KeyStroke f1 = KeyStroke.getKeyStroke(KeyEvent.VK_F1, 0, false);
        Action f1Action = new AbstractAction() {
            public void actionPerformed(ActionEvent e) {
                // el que parsea
                SimpleDateFormat parseador = new SimpleDateFormat("dd/MM/yyyy");
                // el que formatea
                SimpleDateFormat formateador = new SimpleDateFormat("YYYY-MM-dd");

                if (txtFechaInicial.getDatoFecha()== null || txtFechaFinal.getDatoFecha()== null) {
                    ErrorAlert e1 = new ErrorAlert(new JFrame(), true);
                    e1.msj1.setText("Selecciona un rango de fechas");
                    e1.msj2.setText("para generar el reporte.");
                    e1.msj3.setText("");
                    e1.setVisible(true);
                } else {

                    if (txtFechaInicial.getDatoFecha().before(txtFechaFinal.getDatoFecha()) || txtFechaInicial.getDatoFecha().equals(txtFechaFinal.getDatoFecha())) {
                        String fecha1 = String.valueOf(formateador.format(txtFechaInicial.getDatoFecha()));
                        String fecha2 = String.valueOf(formateador.format(txtFechaFinal.getDatoFecha()));

                        new Thread(new Runnable() {
                            @Override
                            public void run() {
                                c.setVisible(true);
                            }
                        }).start();

                        new Thread(new Runnable() {
                            @Override
                            public void run() {
                                reporteVentas(fecha1, fecha2);
                            }
                        }).start();
                    } else {
                        ErrorAlert e1 = new ErrorAlert(new JFrame(), true);
                        e1.msj1.setText("Selecciona un rango de fechas v치lido");
                        e1.msj2.setText("para generar el reporte.");
                        e1.msj3.setText("");
                        e1.setVisible(true);
                    }
                }
            }
        };
        this.getInputMap(JComponent.WHEN_IN_FOCUSED_WINDOW).put(f1, "F1");
        this.getActionMap().put("F1", f1Action);
    }

